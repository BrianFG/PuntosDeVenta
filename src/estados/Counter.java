package estados;

public class Counter {
	private int NoCruceros;
	private int NoPeriodicoCruceros;
	private int NoLCs;
	private int NoPeriodicoLC;
	private int NoMujeres;
	private int NoPerioricoMujeres;
	
	public Counter(int noCruceros, int noPeriodicoCruceros, int noLCs,
			int noPeriodicoLC, int noMujeres, int noPerioricoMujeres) {
		super();
		NoCruceros = noCruceros;
		NoPeriodicoCruceros = noPeriodicoCruceros;
		NoLCs = noLCs;
		NoPeriodicoLC = noPeriodicoLC;
		NoMujeres = noMujeres;
		NoPerioricoMujeres = noPerioricoMujeres;
	}

	public int getNoCruceros() {
		return NoCruceros;
	}

	public void setNoCruceros(int noCruceros) {
		NoCruceros = noCruceros;
	}

	public int getNoPeriodicoCruceros() {
		return NoPeriodicoCruceros;
	}

	public void setNoPeriodicoCruceros(int noPeriodicoCruceros) {
		NoPeriodicoCruceros = noPeriodicoCruceros;
	}

	public int getNoLCs() {
		return NoLCs;
	}

	public void setNoLCs(int noLCs) {
		NoLCs = noLCs;
	}

	public int getNoPeriodicoLC() {
		return NoPeriodicoLC;
	}

	public void setNoPeriodicoLC(int noPeriodicoLC) {
		NoPeriodicoLC = noPeriodicoLC;
	}

	public int getNoMujeres() {
		return NoMujeres;
	}

	public void setNoMujeres(int noMujeres) {
		NoMujeres = noMujeres;
	}

	public int getNoPerioricoMujeres() {
		return NoPerioricoMujeres;
	}

	public void setNoPerioricoMujeres(int noPerioricoMujeres) {
		NoPerioricoMujeres = noPerioricoMujeres;
	}
	
	
	
	
	

	
	

}
